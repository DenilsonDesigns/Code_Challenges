const mergeEntriesByDate = (firstArr, secondArr) => {
  const mergedArray = firstArr;
  const firstArrDates = firstArr.map((el) => el.dateOccurred);

  secondArr.forEach((element) => {
    if (firstArrDates.includes(element.dateOccurred)) {
      // push the transactions to this date in mergedArr
      const foundIndex = mergedArray.findIndex(
        (el) => el.dateOccurred === element.dateOccurred
      );
      console.log("found index: ", foundIndex);
      mergedArray[foundIndex]["transactions"] = [
        ...mergedArray[foundIndex]["transactions"],
        element.transactions,
      ];
    } else {
      mergedArray.push(element);
    }
  });

  return mergedArray;
};

